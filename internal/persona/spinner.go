// GetSpinnerVerbs generates full spinner verbs by combining stems
// with persona-specific suffixes.
// Tries file-based spinner loading first, falls back to hardcoded stems.
func GetSpinnerVerbs(personaType string) []string {
	// Try file-based loading first
	if personaDir := ResolveDir(); personaDir != "" {
		if sd, err := LoadSpinner(personaDir, personaType); err == nil {
			if verbs := sd.BuildSpinnerVerbs(); len(verbs) > 0 {
				return verbs
			}
		}
	}

	// Fallback to hardcoded stems
	if personaType == "young-f" {
		verbs := make([]string, len(SpinnerStemsYoungF))
		for i, e := range SpinnerStemsYoungF {
			var suffix string
			switch i % 3 {
			case 0:
				suffix = "ì¤‘ì´ì—ìš”!"
			case 1:
				suffix = "ì¤‘ì´ì—ìš” ì„ ë°°!"
			case 2:
				suffix = "ì¤‘!"
			}
			if e.Emoji != "" {
				verbs[i] = e.Stem + suffix + " " + e.Emoji
			} else {
				verbs[i] = e.Stem + suffix
			}
		}
		return verbs
	}

	// Other personas use default stems
	stems := SpinnerStemsDefault
	verbs := make([]string, len(stems))

	for i, stem := range stems {
		switch personaType {
		case "young-m":
			if i%2 == 0 {
				verbs[i] = stem + "ì¤‘ì…ë‹ˆë‹¤!"
			} else {
				verbs[i] = stem + "ì¤‘ì…ë‹ˆë‹¤ ì„ ë°°ë‹˜!"
			}
		case "senior-f":
			verbs[i] = stem + "ì¤‘ì…ë‹ˆë‹¤."
		case "senior-m":
			verbs[i] = stem + "ì¤‘ì…ë‹ˆë‹¤."
		default:
			verbs[i] = stem + "ì¤‘ì´ì—ìš”!"
		}
	}

	return verbs
}
